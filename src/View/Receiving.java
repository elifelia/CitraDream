    private void RRTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RRTableMouseClicked
        // TODO add your handling code here:
        RRDetailBean pr = new RRDetailBean();
        RRDetailBean temp;
        int row = RRTable.getSelectedRow();
        temp = pr.cariRRdetail(RRTable.getModel().getValueAt(row, 0).toString(), RRTable.getModel().getValueAt(row, 1).toString());
        po.setText(temp.getPO_Number());
        item.setText(temp.getItem_id());
        qtyReceived.setText(String.valueOf(temp.getQtyReceived()));
        qtyOnHold.setText(String.valueOf(temp.getQtyOnHold()));
        department.setSelectedItem(temp.getDepartment_id());
    }//GEN-LAST:event_RRTableMouseClicked

    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        RRDetailBean pr = new RRDetailBean();
        if (pr.addRRdetail(RR.getText(), po.getText(), item.getText(), 
                Double.parseDouble(qtyReceived.getText()), 
                Double.parseDouble(qtyOnHold.getText()), supplier.getText(), 
                department.getSelectedItem().toString())) {
        JOptionPane.showMessageDialog(null, "Item add succeded!");
        po.setText("");
        item.setText("");
        item.setText("");
        qtyReceived.setText("");
        qtyOnHold.setText("");
        supplier.setText("");
        department.setSelectedItem("");
        } else {
            Logger.getLogger(TableUser.class.getName()).log(Level.SEVERE, null, new SQLException());
        }
    }//GEN-LAST:event_addActionPerformed

