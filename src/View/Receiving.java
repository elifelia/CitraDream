    private void fillCombo() {
        try {
            DatabaseConnection db = new DatabaseConnection();
            String query = "SELECT department_id FROM hcdy_department";
            Connection con = db.getConnection();
            PreparedStatement pst = con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                String dept = rs.getString("department_id");
                department.addItem(dept);
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
    
    public void DBtoTable() {
        DatabaseConnection db = new DatabaseConnection();

        try {
            String query = "SELECT * FROM hcdy_receivingdetail WHERE RR_Number = '" + RR.getText() + "'";
            PreparedStatement pst = db.getConnection().prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            RRTable.setModel(DbUtils.resultSetToTableModel(rs));
            db.getConnection().close();
        } catch (SQLException ex) {
            Logger.getLogger(TableUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

