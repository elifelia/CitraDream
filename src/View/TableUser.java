    private class PassListener implements DocumentListener {

        @Override
        public void insertUpdate(DocumentEvent e) {
            if (new_pass.getText().equals(passconf.getText())) {
                warn.setVisible(true);
                warn.setForeground(Color.GREEN);
                warn.setText("Password Match!");
            } else {
                warn.setVisible(true);
                warn.setForeground(Color.RED);
                warn.setText("Password doesn't Match!");
            }
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            if (new_pass.getText().equals(passconf.getText())) {
                warn.setVisible(true);
                warn.setForeground(Color.GREEN);
                warn.setText("Password Match!");
            } else {
                warn.setVisible(true);
                warn.setForeground(Color.RED);
                warn.setText("Password doesn't Match!");
            }
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            if (new_pass.getText().equals(passconf.getText())) {
                warn.setVisible(true);
                pass.setForeground(Color.GREEN);
                pass.setText("Password Match!");
            } else {
                warn.setVisible(true);
                warn.setForeground(Color.RED);
                warn.setText("Password doesn't Match!");
            }
        }
    }

